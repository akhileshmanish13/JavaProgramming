package com.company;

public class HelloWorld {

    public static void main(String[] args) {
        // Print the output in same line using "PRINT" function
        System.out.print("Hello World from Home ");

        // Print the output in new line using "PRINTLN" function
        System.out.println("Hello World");
        System.out.println("from Home");

        // Print the output in new line using "\n"
        System.out.print("Hello \nworld \nfrom \nHome\n");

        // Print a * format
        System.out.print("*\n**\n***\n****");

    }
}
